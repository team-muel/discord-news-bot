create or replace function public.evaluate_platform_hypothetical_indexes(p_index_ddls text[])
returns table (
  ddl text,
  indexrelid bigint,
  estimated_size_bytes bigint,
  status text,
  message text
)
language plpgsql
security definer
set search_path = public, pg_catalog
as $$
declare
  has_hypopg boolean;
  ddl_item text;
  created_oid bigint;
begin
  select exists (select 1 from pg_extension where extname = 'hypopg') into has_hypopg;
  if not has_hypopg then
    return;
  end if;

  perform hypopg_reset();

  foreach ddl_item in array coalesce(p_index_ddls, '{}') loop
    begin
      select * into created_oid from hypopg_create_index(ddl_item);
      return query
      select
        ddl_item,
        created_oid,
        coalesce(hypopg_relation_size(created_oid), 0),
        'ok'::text,
        'hypothetical index created'::text;
    exception when others then
      return query
      select
        ddl_item,
        null::bigint,
        null::bigint,
        'error'::text,
        left(sqlerrm, 300);
    end;
  end loop;
end;
$$;

