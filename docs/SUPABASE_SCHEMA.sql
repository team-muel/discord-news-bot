create or replace function public.delete_orphaned_source()
returns trigger
language plpgsql
as $$
begin
  if not exists (select 1 from public.alert_slots where source_id = old.source_id) then
    delete from public.sources where id = old.source_id;
  end if;
  return old;
end;
$$;

